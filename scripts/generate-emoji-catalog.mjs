import { writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const EMOJI_VERSION = "17.0";
const SOURCE_URL = `https://www.unicode.org/Public/${EMOJI_VERSION}.0/emoji/emoji-test.txt`;
const OUTPUT_PATH = resolve("packages/client/src/lib/emoji-catalog.generated.ts");
const SKIN_TONE_RANGE = [0x1f3fb, 0x1f3ff];
const CATEGORY_ICONS = {
  "Smileys & Emotion": "😀",
  "People & Body": "👋",
  "Animals & Nature": "🐾",
  "Food & Drink": "🍕",
  "Travel & Places": "🚗",
  Activities: "⚽",
  Objects: "💡",
  Symbols: "💯",
  Flags: "🏁",
};

const response = await fetch(SOURCE_URL);
if (!response.ok) throw new Error(`Could not download Unicode emoji data: ${response.status} ${response.statusText}`);
const source = await response.text();
const categories = new Map();
const names = {};
let activeGroup = "";

for (const line of source.split(/\r?\n/u)) {
  if (line.startsWith("# group:")) {
    activeGroup = line.slice("# group:".length).trim();
    continue;
  }
  const match = line.match(/^([0-9A-F ]+)\s*;\s*fully-qualified\s*#\s*\S+\s+E[0-9.]+\s+(.+)$/u);
  if (!match || !(activeGroup in CATEGORY_ICONS)) continue;
  const codePoints = match[1].trim().split(/\s+/u).map((value) => Number.parseInt(value, 16));
  if (codePoints.some((codePoint) => codePoint >= SKIN_TONE_RANGE[0] && codePoint <= SKIN_TONE_RANGE[1])) continue;
  const emoji = String.fromCodePoint(...codePoints);
  const name = match[2].trim().toLowerCase();
  const entries = categories.get(activeGroup) ?? [];
  entries.push(emoji);
  categories.set(activeGroup, entries);
  names[emoji] = name;
}

const catalog = Object.entries(CATEGORY_ICONS).map(([label, icon]) => ({
  label,
  icon,
  emojis: categories.get(label) ?? [],
}));
const emojiCount = catalog.reduce((total, category) => total + category.emojis.length, 0);
if (emojiCount < 1_900 || !names["🧪"]) {
  throw new Error(`Unicode emoji catalog was incomplete: received ${emojiCount} base emoji.`);
}

const output = `// Generated by scripts/generate-emoji-catalog.mjs from Unicode Emoji ${EMOJI_VERSION}.
// Source: ${SOURCE_URL}
// Skin-tone variants are omitted; base emoji and non-tone sequences remain available.

export const EMOJI_CATEGORIES = ${JSON.stringify(catalog, null, 2)} as const;

export const EMOJI_SEARCH_NAMES: Readonly<Record<string, string>> = ${JSON.stringify(names, null, 2)};
`;

await writeFile(OUTPUT_PATH, output, "utf8");
process.stdout.write(`Generated ${emojiCount} Unicode Emoji ${EMOJI_VERSION} entries.\n`);
