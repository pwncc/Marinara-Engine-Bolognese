  id: "character-book",
  name: "Assigned Character Book",
  description: "A character-linked lorebook.",
  category: "character",
  imagePath: null,
  scanDepth: 4,
  tokenBudget: 3072,
  entryLimit: 50,
  recursiveScanning: true,
  maxRecursionDepth: 5,
  excludeFromVectorization: false,
  vectorQueryDepth: 12,
  vectorScoreThreshold: 0.42,
  vectorMaxResults: 9,
  characterId: "character-1",
  characterIds: ["character-1"],
  personaId: null,
  personaIds: [],
  chatId: null,
  isGlobal: false,
  enabled: true,
  scope: { mode: "all", chatIds: [] },
  tags: ["assigned"],
  generatedBy: "user",
  sourceAgentId: null,
  createdAt: "2026-07-10T10:00:00.000Z",
